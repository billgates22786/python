class DataTypes {
    public static void main(String[] args) {
        int apple = 50;
        byte num = 1;
        short height = 180;
        long person = 2580000;
        float marks = 85.36f;
        double percentage = 96.25555;
        char x = 'a';
        boolean isValid = true;

        System.out.println("apple = " + apple + ", num = " + num + ", height = " + height +", person = " + person + ", marks = " + marks + ", percentage = " + percentage +", x = " + x + ", isValid = " + isValid);
    }
}
